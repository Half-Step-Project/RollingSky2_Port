namespace RS2
{
	public class CommonHelpData
	{
		private string m_Title;

		private string m_content;

		public string Title
		{
			get
			{
				return m_Title;
			}
			set
			{
				m_Title = value;
			}
		}

		public string Content
		{
			get
			{
				return m_content;
			}
			set
			{
				m_content = value;
			}
		}
	}
}
